    <h4 class="text-center">Conhecendo o diretor do (SAAE) Serviço Autônomo de Água e Esgoto de Cametá</h4>
    <br>
    <p class="text-justify" style="text-indent: 10%;">
        O diretor do SAAE Cametá é o senhor Loucelino de Pinho Gonçalves, nascido na
        cidade de Cametá, filho de pais humildes, fez seu primário na Escola municipal D.
        Raimundo de Seixas, formou-se tecnólogo em processos gerais pela FATEC.
        Com muito estudo e dedicação conseguiu passa no concurso da FUNASA para a cidade
        de Belém-PA, onde desempenhou varias funções mais dentre de muitos, a mais
        importante e mais longa foi no departamento de engenharia na UECQA – UNIDADE
        DE CONTROLE DE TRATAMENTO DE ÁGUA DOS MUNICÍPIOS, onde teve seus
        primeiros contatos com controle de água para a população.</p>

    <p class="text-justify" style="text-indent: 10%;">
        Só que como todo bom cametaense sempre quis voltar para sua cidade amada para de
        alguma forma contribuir para a melhoria do município, assim no ano de 2005 foi
        convidado pelo recém-eleito prefeito de Cametá excelentíssimo senhor José Waldoli
        Filgueira Valente para ser o diretor do Serviço Autônomo de Água e Esgoto de Cametá
        (SAAE), O mesmo ficou a frente do SAAE de 2005 a 2012, ainda assim sem medir
        esforços trabalhou para a melhoria do sistema de abastecimento de água de Cametá, em
        2017 foi convidado pelo excelentíssimo senhor José Waldoli Filgueira Valente para
        assumi o SAAE CAMETÁ novamente.</p>

    <p class="text-justify" style="text-indent: 10%;">
        Um de seus objetivos maior na sua atual gestão é manter um constante aperfeiçoamento
        técnico, para a completa satisfação da população e suas maiores preocupações, dentre
        tantas outras, são com relação à aplicação dos melhores e mais modernos métodos
        visando manter a qualidade da água à realização de obras para manutenção e ampliação
        do sistema de saneamento básico do município e vilas. Assim toda água que chega a sua
        residência é devidamente tratada, pois preocupa - se com a qualidade e fornecimento
        deste recurso (finito) para que esteja sempre disponível para o consumo de seus
        usuários.</p>
    </p>

