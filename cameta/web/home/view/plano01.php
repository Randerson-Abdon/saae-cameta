    <p class="font-weight-bold">
        Controle da Cloração
    </p>

    <p class="text-justify" style="text-indent: 10%;">
        Como a maioria das comunidades que já utilizam esse equipamento é abastecida com
        água subterrânea, recomenda-se que a dosagem máxima de cloro fique em torno de 1,0
        mg/L, para que, após o tempo de contato, se obtenha na saída do tratamento, no mínimo
        0,5 mg/L. Em algumas situações isso não será possível, principalmente em águas que
        possuem elevadas concentrações de compostos orgânicos e inorgânicos. Nesse caso, é
        necessário adicionar uma quantidade maior de cloro evitando que se ultrapasse o teor
        máximo permitido para consumo humano.
    </p>



