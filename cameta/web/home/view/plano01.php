    <h4 class="text-center">Tratamento de Água para o seu Consumo</h4>
    <br>
    <p class="text-justify" style="text-indent: 10%;">
        O Serviço Autônomo de Água e Esgoto (SAAE) – Cametá Realiza semanalmente o
        tratamento de água nos poços artesianos, o processo ocorre de maneira manual no total
        são nove poços com exceção de um que o processo é automatizado. Sendo este
        localizado na Rua 15 de novembro, possui um <strong>Gerador de Cloro Hidrogeron</strong>
        (Hipoclorito de Sódio) produz e aplica o cloro de forma segura e sustentável,
        assegurando um nível estável de cloro até a ponta da rede.
    </p>

    <p class="text-justify" style="text-indent: 10%;">
        O gerador de cloro é capaz de atender qualquer vazão de cloração, a tecnologia vai de
        encontrar às necessidades de quem precisa ter mais controle, automação, segurança,
        melhoria de processos, teor residual e redução de custos operacionais.
    </p>

    <br>
    <p class="font-weight-bold">
        Operação e Benefícios
    </p>

    <p class="text-justify" style="text-indent: 10%;">
        A produção ocorre a partir da eletrólise da salmoura. Utiliza somente água, energia,
        elétrica e sal (cloreto de sódio), insumo de fácil acesso e baixo custo
        A operação é simples, segura e automatizada, dispensando transporte, armazenamento e
        manuseio do produto químico.
    </p>

    <p class="text-justify" style="text-indent: 10%;">
        Seguindo o percurso de tratamento de água manual o 2° poço fica localizado na Rua
        Inácio Moura, Bairro da Aldeia. (bomba do Basa). O tratamento é feito com pastilhas de
        cloro. O processo básico da cloração consiste em utilizar produtos químicos à base de
        cloro, com o objetivo de inativar os micro-organismos patogênicos existentes na água.
        Além da função básica, o cloro é um poderoso oxidante e assim reagem com grande
        número de substâncias orgânicas e inorgânicas presentes na água, como por exemplo, na
        remoção de gás sulfídrico, ferro e manganês.
    </p>

    <p class="text-justify" style="text-indent: 10%;">
        3º Poço de tratamento de água manual localizada também na Inácio Moura, Bairro da
        Aldeia (SEPLAC).
    </p>

    <br>
    <p class="font-weight-bold">
        O clorador simplificado desenvolvido pela Funasa
    </p>

    <p class="text-justify" style="text-indent: 10%;">
        O clorador simplificado desenvolvido pela Funasa foi uma adaptação do clorador de
        pastilha, para utilizar solução de hipoclorito de cálcio [Ca(OH)2 ] ou hipoclorito de
        sódio (NaOCl) como desinfetante. É mais um instrumento que serve para adicionar o
        cloro na água de modo seguro, sem que haja necessidade de instalação elétrica,
        preocupação constante com o controle da dosagem, nem operação complexa. É
        construído de material hidráulico (tubos e conexões), disponíveis no mercado.
        O clorador foi desenvolvido para ser utilizado em tratamento de volumes fixos de água
        proveniente de manancial subterrâneo, captada por meio de poços tubulares profundos,
        rasos ou escavados.
    </p>

    <p class="text-justify">
        4º Poço de tratamento de água manual localizada no Bairro de Castanhal. O
        funcionamento do clorador simplificado é semelhante ao de um clorador de pastilha.
        Porém, ao invés de usar pastilhas de cloro, utiliza-se uma solução de hipoclorito de
        cálcio na concentração desejada, de tal modo que a dosagem aplicada fique em torno de
        1,0 mg/L, a qual, após o tempo de contato recomendado na legislação resulte num teor
        de, no mínimo, 0,2 mg/L em toda a extensão do sistema de distribuição (reservatório e
        rede).
    </p>

