    <h4 class="text-center">SOBRE O SAAE</h4>
    <br>
    <p class="text-justify" style="text-indent: 10%;">
        Seja bem vindo ao site do Serviço Autônomo de Água e Esgoto de
        Cametá (SAAE). Um ambiente desenvolvido para facilitar o acesso da população
        cametaense aos serviços deste SAAE, temos atendentes online no
        horário de segunda a sexta - feira das 7h30min às 12 h e das 14 h às 17
        h, para tirar suas duvidas sobre fatura, extrato, segunda via de fatura,
        tudo para a comodidade de seus usuários.
    </p>

    <p class="text-justify" style="text-indent: 10%;">
        O Serviço Autônomo de Água e Esgoto de Cametá (SAAE) foi criado pela
        Lei Municipal nº 118, de 18 de agosto de 1961 com o propósito de cuidar
        da operação, manutenção, expansão e reparos dos sistemas de água, e
        arrecadar dos usuários deste SAAE as taxas estipuladas pelos seus
        serviços prestados, para que, com essa receita possa cumprir com o
        propósito de sua criação, tendo total exclusividade do monopólio para
        exploração dos serviços de água e esgoto deste Município.
    </p>

    <p class="text-justify" style="text-indent: 10%;">
        Da criação natureza e finalidade do Serviço Autônomo de Água e Esgoto:
        O Serviço Autônomo de Água e Esgoto (SAAE) foi criado através de Lei
        Municipal, passando a ser uma Autarquia Pública com personalidade
        jurídica própria e autonomia administrativa e financeira, constituída de
        três seções que são: Administrativa, Financeira - Contábil e Técnica.
    </p>

    <p class="text-justify" style="text-indent: 10%;">
        A seção Administrativa compete: manter seu quadro funcional em perfeita
        harmonia com as atividades desta autarquia, controlar as despesas de
        acordo com a receita, para que haja um equilíbrio proporcional, fiscalizar
        e manter sempre ativa e satisfatória as atividades propostas por este
        SAAE, manter em dias e as claras junto aos órgãos fiscalizadores e ao
        público em geral, suas contas e atividades.
    </p>

    <p class="text-justify" style="text-indent: 10%;">
        A seção Financeira - Contábil compete: registro orçamentário,
        escrituração da receita arrecadada e a arrecadar, documentação e
        escrituração das despesas e pagar de acordo com os dispositivos que
        regulam a matéria, registro da movimentação de materiais no
        almoxarifado, processamento das contas do fornecimento, preparo das
        contas de medição, registro dos valores patrimoniais e levantamento
        periódico do seu inventário e estado, expedir e entregar as contas mensais
        dos serviços de águas e esgotos, preparadas de acordo com as tabelas que
        as regulam, anotar o consumo de água, arrecadar as rendas do SAAE.
    </p>

    <p class="text-justify" style="text-indent: 10%;">
        A seção Técnica compete: manter as boas condições sanitárias e dos
        mananciais utilizados no abastecimento, conservar em toda sua eficiência
        as instalações de bombeamento e tratamento de água, conservar e reparar
        as linhas adutoras e distribuidoras, os reservatórios, hidrantes,
        hidrômetros, torneiras publicas, registros e demais pertences do sistema,
        instalar e fiscalizar os ramais domiciliário, comunicar a seção financeira
        das irregularidades verificadas nas instalações domiciliárias e, quando for
        o caso, aplicar penalidades e providenciar os seus reparos, efetuar o corte
        do serviço ou conexão quando a seção financeira expedir ordem para isto,
        colher amostras de água para testes físico-químico e bacteriológico e
        examiná-las.
    </p>

    <p class="text-justify" style="text-indent: 10%;">
        Do abastecimento de água: A água servida aos usuários deste SAAE, é de
        origem subterrânea, captada de poços através de bombas e armazenadas
        em caixas elevadas, sendo sua distribuição feita por meio de gravidade e
        ou impulsionadas nas redes por bombas adequadas, e adicionada a mesma,
        reagentes químicos, com a finalidade de evitar contaminações de
        percurso. O controle da água servida por este Serviço é feito
        sistematicamente por meio de exames físico-químico e bacteriológico, no
        período de 03 em 03meses, através da FUNASA.
    </p>

    <p class="text-justify" style="text-indent: 10%;">
        A distribuição e medição do volume de água fornecida aos usuários é
        procedida e verificada da seguinte maneira: Conexão: A tubulação,
        registro e demais peças por intermédio das quais a água é conduzida do
        ramal público ao hidrômetro ou até o registro de entrada nos serviços não
        medidos. Consumo mínimo mensal: Volume de água atribuído como
        mínimo indispensável para atender ás necessidades orgânicas e higiênicas
        de uma família e sua habitação, mensalmente. Contribuinte: pessoa física
        ou jurídica que se utiliza dos serviços do SAAE. Corte de conexão:
        Interrupção do fornecimento de água pela retirada de toda a conexão
        domiciliaria ou parte desta. Descrição do serviço: O SAAE envidará todos
        os esforços no sentido de fornecer água suficiente aos contribuintes, de
        maneira continua e sob pressão adequada, evitando escassez no
        abastecimento.
    </p>

